// Question 75: Compound Assignment Operators: Use compound assignment operators to simplify arithmetic operations. Start with x = 4 and perform a series of operations (addition, subtraction, multiplication, division) on x using compound operators.

let x = 4;

// Addition: x = x + 2
x += 2;
console.log("After Addition, x =", x); // Output: After addition, x = 6

// Subtraction: x = x - 3
x -= 3;
console.log("After Subtraction, x =", x); // Output: After subtraction, x = 3

// Multiplication: x = x * 5
x *= 5;
console.log("After Multiplication, x =", x); // Output: After multiplication, x = 15

// Division: x = x / 2
x /= 2;
console.log("After Division, x =", x); // Output: After division, x = 7.5 